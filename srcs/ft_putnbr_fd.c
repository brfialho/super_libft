#include "../headers/libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		n *= -1;
		write(fd, "-", 1);
	}
	if (n / 10)
		ft_putnbr_fd(n / 10, fd);
	write(fd, (char *)&"0123456789"[n % 10], 1);
}

